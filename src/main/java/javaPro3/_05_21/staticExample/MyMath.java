package javaPro3._05_21.staticExample;

public class MyMath {

    // static - используется для указания, что наш метод принадлежит классу, а не экземпляру класса (т.е. static метод может быть доступен без  создания экземпляра класса, а через его наименование )


    public static int sum(int x, int y) {
        return x + y;
    };

    public static int multi(int x, int y) {
        return x * y;
    };


}
