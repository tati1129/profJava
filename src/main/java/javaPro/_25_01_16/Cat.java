package javaPro._25_01_16;

public class Cat {

    private String name;
    private  int age;
    private int weight;

    public String getName() {
        return name;
    }

    public Cat setName(String name) {
        this.name = name;
        return this;
    }

    public int getAge() {
        return age;
    }

    public Cat setAge(int age) {
        this.age = age;
        return this;
    }

    public int getWeight() {
        return weight;
    }

    public Cat setWeight(int weight) {
        this.weight = weight;
        return this;
    }
}
